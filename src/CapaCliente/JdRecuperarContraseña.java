    public String GenerarCaptcha() {
        Random random = new Random();
        String Captcha = "";
        String letras = "ABCDEFGHIJKL";
        int numero1 = (int) Math.round((Math.random() * 9) + 1);
        int numero2 = (int) Math.round((Math.random() * 9) + 1);
        int numero3 = (int) Math.round((Math.random() * 9) + 1);
        int numero4 = (int) Math.round((Math.random() * 9) + 1);
        int numero5 = (int) Math.round((Math.random() * 9) + 1);
        char letra1 = letras.charAt(random.nextInt(letras.length()));
        char letra2 = letras.charAt(random.nextInt(letras.length()));

        Captcha = String.valueOf(numero1) + String.valueOf(numero2) + String.valueOf(3) + String.valueOf(numero4) + String.valueOf(numero5) + letra1 + letra2;

        return Captcha;
    }

