    int contador = 0;

    private void btnRecargarCaptchaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRecargarCaptchaActionPerformed
        if (contador < 3) {
            String nuevoCaptcha = GenerarCaptcha();
            lblCaptcha.setText(nuevoCaptcha);
            contador++;
            if (contador == 2) {
                JOptionPane.showMessageDialog(this, "Te queda un 1 intento para generar el código Captcha");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Solo puedes Generar 3 veces el código Captcha");
        }
    }//GEN-LAST:event_btnRecargarCaptchaActionPerformed

    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        String respuestaBD = null;
        try {
            respuestaBD = objTrabajador.respuestaRecuperarContra(usuario);
        } catch (Exception ex) {
            Logger.getLogger(JdRecuperarContraseña.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (txtRespuesta.getText().equals("") || txtRptaCaptcha.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "SISTEMA", "Ingrese todos los datos solicitados", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if (respuestaBD.equalsIgnoreCase(txtRespuesta.getText()) && txtRptaCaptcha.getText().equals(lblCaptcha.getText())) {
            psdNvaContraseña.setEnabled(true);
            psdConfirmNvaContraseña.setEnabled(true);
            btnGuardar.setEnabled(true);
            txtRespuesta.setEnabled(false);
            txtRptaCaptcha.setEnabled(false);
            btnConfirmar.setEnabled(false);
            btnRecargarCaptcha.setEnabled(false);
            JOptionPane.showMessageDialog(this, "Datos confirmados, ingrese una nueva contraseña");
        } else {
            if (contador < 3) {
                JOptionPane.showMessageDialog(this, "Respuesta Incorrecta", "SISTEMA", JOptionPane.ERROR_MESSAGE);
                lblCaptcha.setText(GenerarCaptcha());
                contador++;
                if (contador == 2) {
                    JOptionPane.showMessageDialog(this, "Te queda 1 intento", "SISTEMA", JOptionPane.INFORMATION_MESSAGE);
                }
                if (contador == 3) {
                    JOptionPane.showMessageDialog(this, "Error al cambiar contraseña", "SISTEMA", JOptionPane.ERROR_MESSAGE);
                    dispose();
                }
            }
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        String nvaContra = psdNvaContraseña.getText();
        String confirNvaContra = psdConfirmNvaContraseña.getText();

        if (nvaContra.equalsIgnoreCase(confirNvaContra)) {
            try {
                objTrabajador.nuevaContraseña(nvaContra, usuario);
            } catch (Exception ex) {
                Logger.getLogger(JdRecuperarContraseña.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(this, "Contraseña nueva guardada correctamente");
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Verifique que ambas contraseñas sean las mismas");
        }

    }//GEN-LAST:event_btnGuardarActionPerformed


